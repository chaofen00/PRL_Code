function create_first_level_contrasts(first_level_dir)
% Create exact-length contrast vectors after SPM.mat exists.
%
% Confound regressors expand the design matrix, so contrast vectors are
% constructed after estimation using the actual design-matrix length.

spm_mat = fullfile( ...
    first_level_dir, ...
    'SPM.mat' ...
);

if ~exist(spm_mat, 'file')
    error( ...
        'SPM.mat was not found: %s', ...
        spm_mat ...
    );
end

loaded_spm = load(spm_mat);
SPM = loaded_spm.SPM;

column_names = SPM.xX.name;
number_of_columns = size(SPM.xX.X, 2);

outcome_column_index = find_column_index( ...
    column_names, ...
    'Outcome_Onset*bf(1)' ...
);

pe_column_index = find_column_index( ...
    column_names, ...
    'dual_signed_PE' ...
);

if isempty(outcome_column_index)

    fprintf('Design-matrix columns:\n');
    disp(column_names(:));

    error( ...
        'Could not identify the Outcome_Onset main-effect column.' ...
    );
end

if isempty(pe_column_index)

    fprintf('Design-matrix columns:\n');
    disp(column_names(:));

    error( ...
        'Could not identify the dual_signed_PE parametric-modulator column.' ...
    );
end

outcome_weights = zeros( ...
    1, ...
    number_of_columns ...
);

positive_pe_weights = zeros( ...
    1, ...
    number_of_columns ...
);

negative_pe_weights = zeros( ...
    1, ...
    number_of_columns ...
);

outcome_weights(outcome_column_index) = 1;
positive_pe_weights(pe_column_index) = 1;
negative_pe_weights(pe_column_index) = -1;

clear matlabbatch;

matlabbatch{1}.spm.stats.con.spmmat = {
    spm_mat
};

matlabbatch{1}.spm.stats.con.consess{1}.tcon.name = ...
    'Outcome > baseline';

matlabbatch{1}.spm.stats.con.consess{1}.tcon.weights = ...
    outcome_weights;

matlabbatch{1}.spm.stats.con.consess{1}.tcon.sessrep = ...
    'none';

matlabbatch{1}.spm.stats.con.consess{2}.tcon.name = ...
    'dual_signed_PE positive';

matlabbatch{1}.spm.stats.con.consess{2}.tcon.weights = ...
    positive_pe_weights;

matlabbatch{1}.spm.stats.con.consess{2}.tcon.sessrep = ...
    'none';

matlabbatch{1}.spm.stats.con.consess{3}.tcon.name = ...
    'dual_signed_PE negative';

matlabbatch{1}.spm.stats.con.consess{3}.tcon.weights = ...
    negative_pe_weights;

matlabbatch{1}.spm.stats.con.consess{3}.tcon.sessrep = ...
    'none';

matlabbatch{1}.spm.stats.con.delete = ...
    1;

contrast_batch_path = fullfile( ...
    first_level_dir, ...
    'batch_contrasts_dual_signed_PE.mat' ...
);

save( ...
    contrast_batch_path, ...
    'matlabbatch' ...
);

fprintf( ...
    ['Creating contrasts using the exact design length ', ...
     'of %d columns.\n'], ...
    number_of_columns ...
);

fprintf( ...
    'Outcome column index = %d; PE column index = %d\n', ...
    outcome_column_index, ...
    pe_column_index ...
);

spm_jobman( ...
    'run', ...
    matlabbatch ...
);

end


function column_index = find_column_index( ...
    column_names, ...
    required_string ...
)
% Return the first design-matrix column containing required_string.

column_index = [];

for index = 1:numel(column_names)

    current_name = column_names{index};

    if ~isempty( ...
            strfind( ...
                current_name, ...
                required_string ...
            ) ...
        ) %#ok<STREMP>

        column_index = index;
        return;
    end
end

end


