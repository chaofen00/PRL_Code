matlabbatch{1}.spm.stats.factorial_design.multi_cov = ...
    struct( ...
        'files', {}, ...
        'iCFI', {}, ...
        'iCC', {} ...
    );

matlabbatch{1}.spm.stats.factorial_design.masking.tm.tm_none = ...
    1;

matlabbatch{1}.spm.stats.factorial_design.masking.im = ...
    1;

matlabbatch{1}.spm.stats.factorial_design.masking.em = ...
    {''};

matlabbatch{1}.spm.stats.factorial_design.globalc.g_omit = ...
    1;

matlabbatch{1}.spm.stats.factorial_design.globalm.gmsca.gmsca_no = ...
    1;

matlabbatch{1}.spm.stats.factorial_design.globalm.glonorm = ...
    1;

matlabbatch{2}.spm.stats.fmri_est.spmmat = {
    fullfile( ...
        output_directory, ...
        'SPM.mat' ...
    )
};

matlabbatch{2}.spm.stats.fmri_est.write_residuals = ...
    0;

matlabbatch{2}.spm.stats.fmri_est.method.Classical = ...
    1;

matlabbatch{3}.spm.stats.con.spmmat = {
    fullfile( ...
        output_directory, ...
        'SPM.mat' ...
    )
