    contrast_name, ...
    '\s+', ...
    '_' ...
);

second_level_batch_path = fullfile( ...
    output_directory, ...
    [ ...
        'batch_secondlevel_', ...
        safe_contrast_name, ...
        '.mat' ...
    ] ...
);

save( ...
    second_level_batch_path, ...
    'matlabbatch' ...
);

fprintf( ...
    'Running second-level contrast: %s\n', ...
    contrast_name ...
);

fprintf( ...
    'Second-level output: %s\n', ...
    output_directory ...
);

fprintf( ...
    'Number of contrast images: %d\n', ...
    numel(contrast_images) ...
);

spm_jobman( ...
    'run', ...
    matlabbatch ...
);

end


function standardized_values = local_zscore(values)
% Version-stable z-scoring that ignores NaN and Inf.

mean_value = local_nanmean(values);
standard_deviation = local_nanstd(values);

if standard_deviation == 0 ...
        || isnan(standard_deviation)

    standardized_values = ...
        values * 0;
else
    standardized_values = ...
        (values - mean_value) ...
        ./ standard_deviation;
end

end


function mean_value = local_nanmean(values)
% Version-stable mean excluding non-finite values.

values = values(isfinite(values));

if isempty(values)
    mean_value = NaN;
else
    mean_value = mean(values);
end

end


function standard_deviation = local_nanstd(values)
% Version-stable standard deviation excluding non-finite values.

values = values(isfinite(values));

if numel(values) < 2
    standard_deviation = NaN;
else
    standard_deviation = std(values);
end

end


function save_matrix_txt(filename, numeric_matrix)
% Save a numeric matrix as a plain-text file.

file_id = fopen( ...
    filename, ...
    'w' ...
);

if file_id == -1
    error( ...
        'Cannot open file for writing: %s', ...
        filename ...
    );
end

if isempty(numeric_matrix)
    fclose(file_id);
    return;
end

if size(numeric_matrix, 2) == 1

    format_string = ...
        '%.6f\n';

else
    format_string = [
        repmat( ...
            '%.6f ', ...
            1, ...
            size(numeric_matrix, 2) - 1 ...
        ), ...
        '%.6f\n' ...
    ];
end

for row_index = 1:size(numeric_matrix, 1)

    fprintf( ...
        file_id, ...
        format_string, ...
        numeric_matrix(row_index, :) ...
    );
end

fclose(file_id);

end