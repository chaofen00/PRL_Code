function value = clean_tsv_field(value)
% Remove tabs and line breaks before writing a TSV field.

value = char( ...
    string(value) ...
);

value = strrep( ...
    value, ...
    sprintf('\t'), ...
    ' ' ...
);

value = strrep( ...
    value, ...
    sprintf('\r'), ...
    ' ' ...
);

value = strrep( ...
    value, ...
    sprintf('\n'), ...
    ' ' ...
);

end


function sorted_directories = natural_sort_directory_struct( ...
    directory_struct ...
)
% Sort directories by the numeric component of their names.

if isempty(directory_struct)
    sorted_directories = directory_struct;
    return;
end

numeric_values = nan( ...
    numel(directory_struct), ...
    1 ...
);

for index = 1:numel(directory_struct)

    token = regexp( ...
        directory_struct(index).name, ...
        '\d+', ...
        'match', ...
        'once' ...
    );

    if ~isempty(token)
        numeric_values(index) = str2double(token);
    end
end

alphabetical_names = lower( ...
    string( ...
        {directory_struct.name} ...
    ) ...
);

sorting_table = table( ...
    isnan(numeric_values), ...
    numeric_values, ...
    alphabetical_names(:), ...
    (1:numel(directory_struct))', ...
    'VariableNames', ...
    { ...
        'MissingNumber', ...
        'Number', ...
        'Name', ...
        'OriginalIndex' ...
    } ...
);

sorting_table = sortrows( ...
    sorting_table, ...
    { ...
        'MissingNumber', ...
        'Number', ...
        'Name' ...
    } ...
);

sorted_directories = directory_struct( ...
    sorting_table.OriginalIndex ...
);

end


