function stage_file = find_stage_mean_file( ...
    subject_root, ...
    stage_name ...
)
% Locate a stage mean image using supported naming schemes.
%
% Preferred:
%   <subject>/<stage>/pattern_change_mean_<stage>.nii
%
% Alternative:
%   <subject>/<stage>_mean.nii
%
% Both .nii and .nii.gz are supported.

preferred_base = fullfile( ...
    subject_root, ...
    stage_name, ...
    sprintf( ...
        'pattern_change_mean_%s', ...
        stage_name ...
    ) ...
);

alternative_base = fullfile( ...
    subject_root, ...
    sprintf( ...
        '%s_mean', ...
        stage_name ...
    ) ...
);

candidates = {
    [preferred_base, '.nii'], ...
    [preferred_base, '.nii.gz'], ...
    [alternative_base, '.nii'], ...
    [alternative_base, '.nii.gz'] ...
};

stage_file = '';

for candidate_index = 1:numel(candidates)

    if exist(candidates{candidate_index}, 'file') == 2

        stage_file = candidates{candidate_index};
        return;
    end
end

end


function indexed_path = add_volume_index(file_path)
% Add the SPM volume suffix ",1" when it is absent.

file_path = char(file_path);

if ~isempty( ...
        regexp( ...
            file_path, ...
            ',\d+$', ...
            'once' ...
        ) ...
    )

    indexed_path = file_path;
else
    indexed_path = [
        file_path, ...
        ',1' ...
    ];
end

end


function clean_path = remove_volume_index(indexed_path)
% Remove a trailing SPM volume suffix such as ",1".

clean_path = regexprep( ...
    char(indexed_path), ...
    ',\d+$', ...
    '' ...
);

end


function validate_image_grids( ...
    rn_files, ...
    le_files, ...
    subject_names ...
)
% Confirm that all paired maps share the same voxel grid and affine.

reference_path = remove_volume_index( ...
    rn_files{1} ...
);

reference_volume = spm_vol( ...
    reference_path ...
);

reference_dimensions = ...
    reference_volume(1).dim;

reference_affine = ...
    reference_volume(1).mat;

affine_tolerance = 1e-4;

for subject_index = 1:numel(rn_files)

    current_paths = {
        remove_volume_index(rn_files{subject_index}), ...
        remove_volume_index(le_files{subject_index}) ...
    };

    current_labels = {
        'RN', ...
        'LE' ...
    };

    for image_index = 1:numel(current_paths)

        current_volume = spm_vol( ...
            current_paths{image_index} ...
        );

        current_dimensions = ...
            current_volume(1).dim;

        current_affine = ...
            current_volume(1).mat;

        if ~isequal( ...
                current_dimensions, ...
                reference_dimensions ...
            )

            error( ...
                ['Image dimensions do not match the reference grid.\n', ...
                 'Participant: %s\nStage: %s\nFile: %s'], ...
                subject_names{subject_index}, ...
                current_labels{image_index}, ...
                current_paths{image_index} ...
            );
        end

        if max( ...
                abs( ...
                    current_affine(:) ...
                    - reference_affine(:) ...
                ) ...
            ) > affine_tolerance

            error( ...
                ['Image affine does not match the reference grid.\n', ...
                 'Participant: %s\nStage: %s\nFile: %s'], ...
                subject_names{subject_index}, ...
                current_labels{image_index}, ...
                current_paths{image_index} ...
            );
        end
    end
end

fprintf( ...
    'Image-grid validation passed for %d participants.\n', ...
    numel(subject_names) ...
);

end


function sorted_directories = natural_sort_directory_struct( ...
    directory_struct ...
)
% Sort participant folders by the numeric part of their names.

participant_numbers = nan( ...
    numel(directory_struct), ...
    1 ...
);

for directory_index = 1:numel(directory_struct)

    numeric_token = regexp( ...
        directory_struct(directory_index).name, ...
        '\d+', ...
        'match', ...
        'once' ...
    );

    participant_numbers(directory_index) = ...
        str2double(numeric_token);
end

[~, sorting_order] = sort( ...
    participant_numbers ...
);

sorted_directories = directory_struct( ...
    sorting_order ...
);

end


function prepare_output_directory( ...
    output_directory, ...
    overwrite_existing ...
)
% Create the output directory and optionally remove an existing SPM model.

if exist(output_directory, 'dir') ~= 7

    mkdir(output_directory);
    return;
end

existing_spm = fullfile( ...
    output_directory, ...
    'SPM.mat' ...
);

if exist(existing_spm, 'file') == 2

    if overwrite_existing

        files_to_remove = {
            'SPM.mat', ...
            'mask.nii', ...
            'ResMS.nii', ...
            'RPV.nii', ...
            'beta_*.nii', ...
            'con_*.nii', ...
            'ess_*.nii', ...
            'spmT_*.nii', ...
            'spmF_*.nii' ...
        };

        for pattern_index = 1:numel(files_to_remove)

            matching_files = dir( ...
                fullfile( ...
                    output_directory, ...
                    files_to_remove{pattern_index} ...
                ) ...
            );

            for file_index = 1:numel(matching_files)

                file_path = fullfile( ...
                    output_directory, ...
                    matching_files(file_index).name ...
                );

                if exist(file_path, 'file') == 2
                    delete(file_path);
                end
            end
        end

    else
        error( ...
            ['An existing SPM model was found in:\n%s\n', ...
             'Set overwrite_existing_stats=true to replace it.'], ...
            output_directory ...
        );
    end
end

