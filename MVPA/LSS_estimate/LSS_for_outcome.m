function subject_list = detect_subjects_from_functional_files( ...
    functional_root, ...
    task_name, ...
    space_name ...
)
% Detect participant labels from functional filenames.

patterns = {
    sprintf( ...
        'sub-*_task-%s_space-%s_desc-preproc_merged_sm2mm_bold.nii', ...
        task_name, ...
        space_name ...
    ), ...
    sprintf( ...
        'sub-*_task-%s_space-%s_desc-preproc_merged_sm2mm_bold.nii.gz', ...
        task_name, ...
        space_name ...
    ) ...
};

subject_list = {};

for pattern_index = 1:numel(patterns)

    files = dir( ...
        fullfile( ...
            functional_root, ...
            patterns{pattern_index} ...
        ) ...
    );

    for file_index = 1:numel(files)

        subject_token = regexp( ...
            files(file_index).name, ...
            '^(sub-\d+)_task-', ...
            'tokens', ...
            'once' ...
        );

        if ~isempty(subject_token)

            subject_list{end + 1, 1} = ...
                subject_token{1}; %#ok<AGROW>
        end
    end
end

subject_list = unique( ...
    subject_list, ...
    'stable' ...
);

subject_list = natural_sort_subjects( ...
    subject_list ...
);

end


function subject_list = normalize_subject_list(subject_list)
% Normalize participant identifiers to sub-XXX form.

for index = 1:numel(subject_list)

    current_subject = char( ...
        subject_list{index} ...
    );

    digits = regexp( ...
        current_subject, ...
        '\d+', ...
        'match', ...
        'once' ...
    );

    if isempty(digits)

        error( ...
            'Could not parse participant number from: %s', ...
            current_subject ...
        );
    end

    subject_list{index} = sprintf( ...
        'sub-%03d', ...
        str2double(digits) ...
    );
end

subject_list = unique( ...
    subject_list, ...
    'stable' ...
);

subject_list = natural_sort_subjects( ...
    subject_list ...
);

end


function sorted_subjects = natural_sort_subjects(subject_list)
% Sort participant labels in numerical order.

subject_numbers = zeros( ...
    numel(subject_list), ...
    1 ...
);

for index = 1:numel(subject_list)

    digits = regexp( ...
        subject_list{index}, ...
        '\d+', ...
        'match', ...
        'once' ...
    );

    subject_numbers(index) = str2double( ...
        digits ...
    );
end

[~, sorting_order] = sort( ...
    subject_numbers ...
);

sorted_subjects = subject_list( ...
    sorting_order ...
);

end


function functional_file = find_functional_file( ...
    functional_root, ...
    subject_bids, ...
    task_name, ...
    space_name ...
)
% Find a .nii or .nii.gz functional image.

base_name = sprintf( ...
    '%s_task-%s_space-%s_desc-preproc_merged_sm2mm_bold', ...
    subject_bids, ...
    task_name, ...
    space_name ...
);

candidates = {
    fullfile( ...
        functional_root, ...
        [base_name, '.nii'] ...
    ), ...
    fullfile( ...
        functional_root, ...
        [base_name, '.nii.gz'] ...
    ) ...
};

functional_file = '';

for candidate_index = 1:numel(candidates)

    if exist(candidates{candidate_index}, 'file') == 2

        functional_file = candidates{candidate_index};
        return;
    end
end

end


function subject_folder = pick_subject_directory( ...
    root_directory, ...
    number_string ...
)
% Locate a participant directory using common anonymous naming variants:
%
%   Sub02
%   Sub002
%   Sub2

participant_number = str2double( ...
    number_string ...
);

candidates = {
    fullfile( ...
        root_directory, ...
        sprintf('Sub%02d', participant_number) ...
    ), ...
    fullfile( ...
        root_directory, ...
        sprintf('Sub%03d', participant_number) ...
    ), ...
    fullfile( ...
        root_directory, ...
        sprintf('Sub%d', participant_number) ...
    ), ...
    fullfile( ...
        root_directory, ...
        ['Sub', number_string] ...
    ) ...
};

subject_folder = '';

for candidate_index = 1:numel(candidates)

    if exist(candidates{candidate_index}, 'dir') == 7

        [~, subject_folder] = fileparts( ...
            candidates{candidate_index} ...
        );

        return;
    end
end

end


function valid = is_valid_nifti(nifti_path)
% Return true only when a NIfTI exists and can be read by SPM.

valid = false;

if exist(nifti_path, 'file') ~= 2
    return;
end

try
    volume = spm_vol(nifti_path);

    valid = ...
        ~isempty(volume) ...
        && isfield(volume, 'dim') ...
        && numel(volume(1).dim) == 3;

catch
    valid = false;
end

end


function write_log(log_file, message)
% Append one line to a text log.

file_id = fopen( ...
    log_file, ...
    'a' ...
);

if file_id == -1

    warning( ...
        'Cannot open log file: %s', ...
        log_file ...
    );

    return;
end

fprintf( ...
    file_id, ...
    '%s\n', ...
    message ...
);

fclose(file_id);

end


function write_trial_error( ...
    trial_directory, ...
    trial_index, ...
    error_message, ...
    error_stack ...
)
% Save a trial-level error report.

if exist(trial_directory, 'dir') ~= 7
    mkdir(trial_directory);
end

error_file = fullfile( ...
    trial_directory, ...
    'error.txt' ...
);

file_id = fopen( ...
    error_file, ...
    'w' ...
);

if file_id == -1
    return;
end

fprintf( ...
    file_id, ...
    'Trial %d failed.\n\n', ...
    trial_index ...
);

fprintf( ...
    file_id, ...
    'Error message:\n%s\n\n', ...
    error_message ...
);

if ~isempty(error_stack)

    fprintf( ...
        file_id, ...
        'Stack:\n' ...
    );

    for stack_index = 1:numel(error_stack)

        fprintf( ...
            file_id, ...
            '  at %s:%d\n', ...
            error_stack(stack_index).name, ...
            error_stack(stack_index).line ...
        );
    end
end

fclose(file_id);

end